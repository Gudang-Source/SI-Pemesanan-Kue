<strong><font color="#FF0066">PANDUAN BELANJA</font></strong>
<ol>
  <li>
    <font color="#FF0066">
      Pertama, silahkan Anda mendaftar sebagai member pelanggan di web ini. Caranya, klik <a href="?open=Pelanggan-Baru" target="_self"><strong>Pendaftaran Baru</strong></a> di bawah kotak <strong>LOGIN</strong>. 
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Lengkapi data-data form Pendaftaran, juga Username dan Password harus Anda ingat.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Login menggunakan Username dan Password yang sudah Anda miliki saat mendaftar tadi.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Lakukan pemesanan kue dengan mengklik menu <strong>Beli</strong> yang ada di setiap Kue, dan kue yang sudah dipilih akan masuk ke <a href="?open=Keranjang-Belanja" target="_blank"><strong>Keranjang Belanja</strong></a>, dan Anda dapat mengubah jumlah barangnya, atau juga bisa dihapus jika memang mau dibatalkan.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Jika sudah selesai memilih kue, klik menu/tombol <strong><a href="?open=Transaksi-Lanjut" target="_self">Lanjutkan</a></strong>, lalu Anda akan dibawa ke halaman konfirmasi (form) kemana kue akan dikirim. Silahkan isikan data alamat lenngkap tempat pengiriman kue. Meskipun Anda sudah memiliki data pribadi, akan tetapi pada halaman ini tetap harus Anda isi, karna datanya bisa juga dengan alamat orang lain sebagai tujuan pengiriman. 
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Setelah Pemesanan selesai, kue akan masuk kedaftar Transaksi Pesan.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Setelah itu Admin akan memproses pesanan Anda, Jika kue sedang di pack atau dibungku, maka status Pemesanan Kue Anda aka masih status pesan. Jika dalam proses pengiriman maka status Anda akan diset Menjadi sedang Pegantaran.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Pada saat Anda Menerima barangnya, lakukan Pembayaran atau transaksi kurir.
    </font>
  </li>
  <li>
    <font color="#FF0066">
      Kurir akan menginput hasil transaksi Anda dan Hasilnya dapat dilihat di Sistem. Baik Admin dan User.
    </font>
  </li>
